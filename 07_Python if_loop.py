# Python program to execute for loop

# with start and end
for num in range(1,10):    #the range value starts from 0 and ends in end - 1 value; just like an array
	print(num)


for num in range(1,11):
	print(num)


# without start and end
for num in range(10):
	print("Hello")


# print odd numbers
for num in range(1,20,2):   #range function accepts 3 parameters       range(start,end,step)
	print(num)


# print odd numbers
for num in range(0,20,2):   #range function accepts 3 parameters       range(start,end,step)
	print(num)
